package com.pavan.dto;

public class FacultyNote {
	private String fn_id;
	private String fn_note;
	private String fn_state;
	
	public String getFn_id() {
		return fn_id;
	}
	public void setFn_id(String fn_id) {
		this.fn_id = fn_id;
	}
	public String getFn_note() {
		return fn_note;
	}
	public void setFn_note(String fn_note) {
		this.fn_note = fn_note;
	}
	public String getFn_state() {
		return fn_state;
	}
	public void setFn_state(String fn_state) {
		this.fn_state = fn_state;
	}
	
}
